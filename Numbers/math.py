x = 5

r = x + 1 # 5 + 1 -> 2
r = x - 1 # 5 - 1 -> 4
r = x * 2 # 5 * 2 -> 10
r = x / 2 # 5 / 2 -> 2.5
r = x % 2 # remainder -> 5/2 (resultant = 2; remainder = 1; r =1)
r = x ** 2 # exponentiation -> 5^2 -> 25
r = x // 2 # floor division - > 5/2 (resultant -> 2.5 round down -> 2)

