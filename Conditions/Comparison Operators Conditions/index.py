if 1 == 1: # 1 = 1
    print('1 == 1 is True')

if 1 != 2: # 1 is not equal to 2
    print('1 != 2 is True')

if 2 > 1: # 2 is greater than 1
    print('2 > 1 is True')

if 1 < 2: # 1 is less than 2
    print('1 < 2 is True')

if 1 >= 1: # 1 is greator than OR equal to 1
    print('1 >= 1 is True')

if 1 <= 1: # 1 is less than OR equal to 1
    print('1 <= 1 is True')